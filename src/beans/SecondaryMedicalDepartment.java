package beans;

public class SecondaryMedicalDepartment {
	private int medicalStaffId;
	private String medicalServiceDeptCode;
	
	public int getMedicalStaffId() {
		return medicalStaffId;
	}
	public void setMedicalStaffId(int medicalStaffId) {
		this.medicalStaffId = medicalStaffId;
	}
	public String getMedicalServiceDeptCode() {
		return medicalServiceDeptCode;
	}
	public void setMedicalServiceDeptCode(String medicalServiceDeptCode) {
		this.medicalServiceDeptCode = medicalServiceDeptCode;
	}
	
	
}
