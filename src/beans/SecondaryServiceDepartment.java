package beans;

public class SecondaryServiceDepartment {
	private int nonMedicalStaffId;
	private String departmentCode;
	
	public int getNonMedicalStaffId() {
		return nonMedicalStaffId;
	}
	public void setNonMedicalStaffId(int nonMedicalStaffId) {
		this.nonMedicalStaffId = nonMedicalStaffId;
	}
	public String getDepartmentCode() {
		return departmentCode;
	}
	public void setDepartmentCode(String departmentCode) {
		this.departmentCode = departmentCode;
	}	
}
